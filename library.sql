# Retrieve details of all books in the library â€“ id,
# title, name of publisher, authors, number of copies in each branch,etc.
SELECT B.B_ID, B.TITLE, B.PUB_NAME, A.AUTHOR_NAME, C.NO_OF_COPIES, L.BRANCH_NAME
FROM BOOK B, BOOK_AUTHORS A, BOOK_COPIES C, LIBRARY_BRANCH L
WHERE B.B_ID = A.B_ID
AND B.B_ID = C.B_ID
AND C.BRANCH_ID = L.BRANCH_ID;

# Get the particulars of borrowers who have borrowed more than 3 books, but from Jan2017 to Jun2017.
SELECT CARD_NO
FROM BOOKLENDING
WHERE DATE_OUT BETWEEN '2017-01-01' AND '2017-06-01'
GROUP BY CARD_NO
HAVING COUNT(*)>3;

